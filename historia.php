				<h1 style="font-size: 45px; text-align:center; padding:20px;">Breve historia de las Laptops</h1>
				
				<article class="art1">
					<p>A la hora de hablar de laptops, Bill Moggridge es un nombre que todos deberíamos tener en nuestras mentes, ya que se trata del diseñador inglés a quien se le atribuye la invención de las laptops. En 1982 Bill Moggridge creó la primera laptop de la historia en 1982, la cual llevaba por nombre GRiD Compass 101. Esta computadora portátil contaba con la más alta tecnología de su tiempo. Tenía una pantalla electroluminiscente (ELD) y fue la primera en tener una pantalla grande y brillante. </p>
					<p>Su precio era extremadamente alto -$8,150 dólares- por lo que no era un equipo para todo el público. Sus más frecuentes usuarios fueron ingenieros de la NASA dentro de transbordadores espaciales y militares de los Estados Unidos. Su sistema operativo era muy simple, el MS-DOS BIOS versión A, y el almacenamiento interno era de 384K, mientras que la memoria RAM era de 256K, algo muy impresionante para su época. El procesador con el que trabajaba era un Intel 80186. Pesaba unos 4 kilos.</p>
					<img src="img/00.Laptops/story1.jpg" alt="story1">
				</article>

				<article class="art2">
				<p>Si bien no es la primer “computadora portátil” -dado que Epson y Compaq llevaron al mercado modelos como la HX-20 y la Compaq Portable, respectivamente- su diseño se asemeja a las primeras calculadoras gigantes o computadoras de escritorio miniatura y no a un “cuaderno”. Es por esto que la GRiD Compass se considera la primera laptop de la historia.</p>
				<p>Fue hasta 1986 que la compañía IBM presentó la IBM Convertible PC. La estructura exterior luce muy similar a lo que ya hemos visto; se parece bastante a las máquinas de escribir eléctricas.</p>
				<p>La IBM Convertible PC fue presentada en abril de 1986, costaba poco más de $1,900 dólares y tenía un peso superior al de la mayoría, dado que venía acompañada con una impresora térmica (5Kg). Para interactuar con el sistema –que ya era un PC-DOS Basic– contaba con una pantalla LCD de 640 x 200 pixeles y dos entradas para discos Floppy.</p>	
			</article>

				<article class="art3">
					<h1>¿Por qué lleva el nombre de Convertible? </h1>
				<p>Así como las laptops Asus Transformer a las que les puedes desmontar la pantalla para usarlas como tablet, a la IBM también podías quitarle la pantalla, pero no para usarlas como tablet. De hecho, el monitor desconectado era inútil; podías conectar la parte del teclado a un monitor fijo y convertirla en una computadora de escritorio con impresora incluída. Claro está, para tal acción se necesitaba contar con los adaptadores necesarios, los cuales tenían precios bastante elevados, que iban desde los $190</p>
				<p></p>
				<img src="img/00.Laptops/story2.jpg" alt="story2">
				</article>

				<article class="art4">
				<img src="img/00.Laptops/story3.jpg" alt="story3">
				<p>Para 1991 aparece la primera laptop que marcó tendencia hasta nuestros días y su línea de diseño es la que vemos hoy en día. Se trata de la primer Mac PowerBook. Se introdujo al mercado en octubre de 1991 con un precio que iba de los $2,299 dólares hasta los $4,599. El sistema operativo que traía incluido era Mac OS 7.01 y se podía actualizar a 7.6.1. La pantalla era LCD monocromática de 640 x 400 pixeles, tenía una memoria RAM de 2MB, hasta los 8MB y un almacenamiento de 20MB, hasta los 40MB (razón por la cual los precios de los equipos variaban, tal como ocurre hoy en día).
Este modelo pesaba 6 kilos y fue creado por Apple en conjunto con Sony para lograr un diseño más pequeño, “ligero” y “barato”, pero que en cuanto a rendimiento y capacidades fuera igual Apple Macintosh Portable.</p>
				<p></p>
				</article>

				<article class="art5">
				<p>En 1996 Toshiba creó la Toshiba Libretto 20 y era uno de los modelos más pequeños y realmente ligeros. Pesaba únicamente 840 gramos y ya tenía Windows 95 listo para trabajar. Este modelo se comercializó únicamente en Japón. Estaba equipada con un procesador AMD 485 DX4 que trabajaba a 75MHZ, de 8MB hasta 29MB de RAM y un almacenamiento interno de 270MB. Su pantalla era de 6.1 pulgadas tipo TFT.</p>
				<img src="img/00.Laptops/story4.jpg" alt="story4">
				<p>Fue así como en 30 años se pudo llevar a manos de todos los consumidores equipos completamente portátiles, con capacidades de hasta 16GB en RAM y 1TB de almacenamiento interno. Pantallas Retina, modelos convertibles en tablets e incluso lápices ópticos o stylus para mejorar la experiencia de usuario.</p>
